public class Film {
    private String name;
    private int ageRating;


    public Film(String filName, int filAgeRating){
        this.name = filName;
        this.ageRating = filAgeRating;


    }

    public String name(){
        return name;
    }

    public int ageRating(){
        return ageRating;
    }







}
